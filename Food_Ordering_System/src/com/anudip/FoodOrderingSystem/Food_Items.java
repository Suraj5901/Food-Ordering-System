package com.anudip.FoodOrderingSystem;

public class Food_Items {
	
	
		private String name;
	    private double price;

	    public Food_Items(String name, double price) {
	        this.name = name;
	        this.price = price;
	    }

	    public  String getName() {
	        return name;
	    }

	    public double getPrice() {
	        return price;
	    }

}
